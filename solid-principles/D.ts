// Dependency Inversion
// One should depend upon abstractions, [not] concretions.
// We should always have dependencies on the interfaces, not classes.
// NOTE: Dependency Inversion is not the Dependency Injection!!!
// NOTE: Dependency Inversion is the most important principle in SOLID and all other principles depend on that.
// - Prevent violations of the Single Responsibility Principle (and Seperation of Concerns) [Single Responsiblity pronciple]
// - Enable us to write application that follow Open/Close Principle
// - Allow us to replace one implementation with another [Liskov substitution principle]